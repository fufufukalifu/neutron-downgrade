<p>Berdasarkan hasil tes, menggambarkan bawa minat aktifitasmu kearah aktivitas sosial. Hal inilah yang membuat rekomendasinya ke arah jurusan IPS. Namun, jika ingin mengarahkan ke kelas IPA, diharapkan memikirkan dengan sebaik mungkin. Hasil ini adalah saran dan bukan keputusan mutlak. Keputusan kembali kepada siswa namun asalkan dengan pemikiran matang.
</p>
<h4>BAGAIMANA MEMOTIVASI ANAK ?</h4>
<p>Kenali terlebih dahulu karakterisktik anak. Apakah anak termasuk seorang yang memiliki motivasi internal atau motivasi eksternal. Jika anak termasuk dalam karakteristik motivasi internal, orang tua bisa memberikan tantangan pada anak sebagai strategi mendorong anak untuk mencapai targetnya. Jika anak termasuk seorang yang memiliki motivasi eksternal, orang tua sebaiknya bersikap lebih sabar dalam mendampingi anak, selalu berikan pujian ketika anak mampu menunjukkan keberhasilan sekecil apapaun.</p>

<h4>SISWA SD, BAGAIMANA AGAR ANAK SAYA TIDAK PEMALU ?</h4>
<p>Bisa ditanyakan lebih dalam faktor pemalunya karena apa. Karena dibentuk lingkungan atau karena faktor bawaan dari keluarga. Ketika pemalunya disebabkan karena lingkungan bisa dilakukan dengan memodifikasi lingkungan yaitu membiasakan tergantung potensi anak yang bisa dilakukan untuk membuatnya aktif.</p>

<h4>KENAPA TIDAK DISARANKAN DI JURUSAN IKATAN DINAS ?</h4>
