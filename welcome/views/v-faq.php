<h4>APA ITU MINAT ?</h4>
<p>Minat adalah ketertarikan anak pada suatu hal yang berkaitan dengan kebutuhan atau keinginan yang ada dalam diri anak. Minat terbentuk berdasarkan dari proses belajar, pengalaman, serta aktivitas yang biasa dilakukan.</p>


