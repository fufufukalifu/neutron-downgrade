<h4>KENAPA HASIL PENJURUSAN BERBEDA DENGAN NILAI YANG ADA DI RAPORT ?</h4>
<p>ANAK SAYA NILAI RAPORTNYA MENGARAH KE IPA DAN NILAI IPS JELEK, TAPI KENAPA DI HASILNYA MALAH DIKURUSKAN DI IPS ?
Tes psikologi berbeda dengan tes sekolah. Tes psikologi mengarah pada potensi yang dimiliki oleh seseorang . Sedangkan tes di sekolah merupakan tes prestasi yang bisa dipelajari dan bisa meningkat tergantung setiap orang.
Tes detection bukan mengukur apakah siswa bisa atau tidaknya masuk jurusan IPA atau IPS, akan tetapi tes Detection mengungkapkan seseorang akan mampu bertahan, berkembang dan optimal jika menekuni bidang IPA atau IPS dilihat dari minatnya.
Namun perlu diingat bahwa tes psikologi merupakan rekomendasi yang disarankan berdasarkan potensi dan minat yang dimiliki siswa, pada akhirnya siswa akan memilih jurusan IPA atau IPS dikembalikan kepada siswa yang akan menjalani. Bisa jadi keinginan yang kuat untu masuk jurusan IPA menjadi kelebihan seorang siswa, sehingga dapat didorong sekaligus diajak untuk melihat kekurangan dari nilai-nilai yang perlu ditingkatkan.</p>




<h4>BERAPA KISARAN ANGKANYA JIKA ANAK SAYA KECERDASAN UMUMNYA DIBAWAH RATA-RATA ?</h4>
